export function datepicker_birthdate() {
    $(".datepicker-birthdate").datepicker({
            changeMonth: true,
            changeYear: true,
            maxDate: "-16Y",
            minDate: "-80Y",
            yearRange: "-80:-16",
            onSelect: function () {
                var dateObject = $(this).datepicker('getDate');
                $('#alternate').text(moment(dateObject).format('LL'));

            }

        });

}
